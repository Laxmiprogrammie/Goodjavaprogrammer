package sept.switchcode;

public class Switchcases {
    public static void main(String[] Args){
        int num1=10;
        int num2=20;
        char operator='+';
        switch(operator){
            case '+':
                System.out.println("Addition of two numbers"+(num1+num2));
            case '-':
                System.out.println("Addition of two numbers" + (num1-num2));
            case '*':
                System.out.println("Multiplication of two numbers" + (num1*num2));



        }
    }
}