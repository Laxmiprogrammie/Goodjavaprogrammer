package sept.switchcode;

public class Dayscode {
    public static void main(String[] Args){
        int day=1;
        switch(day){
            case 1:
                System.out.println("Day is monday");
                break;
            case 2:
                System.out.println("Day is tuesday");
                break;
            default:
                System.out.println("With that data no day is present");
                break;
        }
    }
}
